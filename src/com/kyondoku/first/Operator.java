package com.kyondoku.first;

public class Operator {
	public static void main(String[] args) {
		int n1 = 10;
		n1++;
//		값옆에 ++ 하면 1이 올라감
		System.out.println(n1);
		
		++n1;
		System.out.println(n1);
		
		System.out.println("-----------");
		
		int n2 = 10;
		System.out.println(n2 + 1);
		System.out.println(n2);
	}
}
