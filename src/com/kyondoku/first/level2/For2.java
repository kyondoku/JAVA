package com.kyondoku.first.level2;

public class For2 {

	public static void main(String[] args) {
		
		for(int i=0; i<3; i++) {
			
			for(int z=0; z<5; z++) {
				
				for(int r=0; r<3; r++) {
					System.out.printf("%d - %d - %d\n", i, z, r);
					
//					r값 다 돌고나서 z값 1올라가고 z가 다 돌아가면 i가 1 올라간다.
				}
				
				
				
			}
		}

	}

}
